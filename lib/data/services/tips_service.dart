  static String getRandomNutritionTip() {
    return nutritionTips[Random().nextInt(nutritionTips.length)];
  }

  static String getRandomFitnessTip() {
    return fitnessTips[Random().nextInt(fitnessTips.length)];
  }

  static String getRandomTip() {
    final allTips = [...nutritionTips, ...fitnessTips];
    return allTips[Random().nextInt(allTips.length)];
  }

  static List<String> getTipsForMealType(String mealType) {
    final tips = <String>[];

    if (mealType.contains('إفطار') || mealType.contains('breakfast')) {
      tips.addAll([
        'الإفطار الصحي يجب أن يحتوي على بروتين وكربوهيدرات',
        'تناول البيض في الإفطار للحصول على بروتين عالي',
        'أضف الفواكه لإفطارك للحصول على الألياف',
      ]);
    }

    if (mealType.contains('غداء') || mealType.contains('lunch')) {
      tips.addAll([
        'الغداء يجب أن يكون أكبر وجبة في اليوم',
        'تناول البروتين والخضروات في الغداء',
        'تجنب الأطعمة الثقيلة قبل النوم',
      ]);
    }

    if (mealType.contains('عشاء') || mealType.contains('dinner')) {
      tips.addAll([
        'العشاء يجب أن يكون خفيفاً وسهل الهضم',
        'تناول العشاء قبل النوم بساعتين على الأقل',
        'تجنب الدهون الثقيلة في العشاء',
      ]);
    }

    return tips.isNotEmpty ? tips : [getRandomTip()];
  }
}
